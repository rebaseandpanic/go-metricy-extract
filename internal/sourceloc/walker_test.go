// TestWalkGoFiles_DirSymlinkNotFollowed pins the "we inherit filepath.WalkDir's
// no-symlink-follow default" behaviour: a symlinked directory is visited as a
// non-dir entry and its contents are unreachable via the symlink path.
func TestWalkGoFiles_DirSymlinkNotFollowed(t *testing.T) {
	if runtime.GOOS == "windows" {
		t.Skip("symlinks on Windows require elevated privileges")
	}
	root := t.TempDir()
	targetDir := filepath.Join(root, "target")
	targetFile := writeFile(t, root, "target/target.go", goStub)
	if err := os.Symlink(targetDir, filepath.Join(root, "link")); err != nil {
		t.Fatalf("symlink: %v", err)
	}

	got, err := WalkGoFiles(root)
	if err != nil {
		t.Fatalf("WalkGoFiles: %v", err)
	}
	// We expect exactly the real path, not the symlinked one.
	if !reflect.DeepEqual(got, []string{targetFile}) {
		t.Errorf("got %v, want [%s] (symlink should not be followed)", got, targetFile)
	}
}

// TestWalkGoFiles_UnreadableDirSkippedNotFatal pins the W2 fix: a chmod-0
// subdirectory must not abort the whole walk — SkipDir'd silently while
// readable siblings keep producing results.
func TestWalkGoFiles_UnreadableDirSkippedNotFatal(t *testing.T) {
	if os.Geteuid() == 0 {
		t.Skip("root bypasses chmod — cannot exercise permission-denied path")
	}
	root := t.TempDir()
	a := writeFile(t, root, "a.go", goStub)
	locked := filepath.Join(root, "locked")
	writeFile(t, root, "locked/b.go", goStub)
	if err := os.Chmod(locked, 0o000); err != nil {
		t.Fatalf("chmod locked: %v", err)
	}
	// Restore permissions so t.TempDir's cleanup can remove the directory.
	defer os.Chmod(locked, 0o755)

	got, err := WalkGoFiles(root)
	if err != nil {
		t.Fatalf("WalkGoFiles returned error for unreadable subdir (want nil): %v", err)
	}
	if !reflect.DeepEqual(got, []string{a}) {
		t.Errorf("got %v, want [%s] (locked subdir should be skipped silently)", got, a)
	}
}

