func TestWalkGoFiles_DeterministicOrder(t *testing.T) {
	root := t.TempDir()
	// Write in intentionally non-sorted order; the walker must still return
	// sorted output.
	paths := []string{
		writeFile(t, root, "z.go", goStub),
		writeFile(t, root, "a.go", goStub),
		writeFile(t, root, "m.go", goStub),
		writeFile(t, root, "b/c.go", goStub),
		writeFile(t, root, "b/a.go", goStub),
	}
	got, err := WalkGoFiles(root)
	if err != nil {
		t.Fatalf("WalkGoFiles: %v", err)
	}
	if len(got) != len(paths) {
		t.Fatalf("got %d files, want %d (%v)", len(got), len(paths), got)
	}
	if !sort.StringsAreSorted(got) {
		t.Errorf("walk output is not sorted ascending: %v", got)
	}
}

