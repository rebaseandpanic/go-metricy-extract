// writeFile writes content to root/relPath, creating intermediate dirs as
// needed. Fails the test on any I/O error so callers can assume success.
func writeFile(t *testing.T, root, relPath, content string) string {
	t.Helper()
	full := filepath.Join(root, filepath.FromSlash(relPath))
	if err := os.MkdirAll(filepath.Dir(full), 0o755); err != nil {
		t.Fatalf("mkdir %s: %v", filepath.Dir(full), err)
	}
	if err := os.WriteFile(full, []byte(content), 0o644); err != nil {
		t.Fatalf("write %s: %v", full, err)
	}
	return full
}

// goStub is a minimal valid .go file body. Content does not matter for the
// walker — only file name, dir position, and (for IsGenerated) first lines.
const goStub = "package p\n"

func TestWalkGoFiles_ReturnsAllGoFiles(t *testing.T) {
	root := t.TempDir()
	a := writeFile(t, root, "a.go", goStub)
	b := writeFile(t, root, "sub/b.go", goStub)
	c := writeFile(t, root, "sub/nested/c.go", goStub)

	got, err := WalkGoFiles(root)
	if err != nil {
		t.Fatalf("WalkGoFiles: %v", err)
	}
	want := []string{a, b, c}
	sort.Strings(want)
	if !reflect.DeepEqual(got, want) {
		t.Errorf("files mismatch:\n got %v\nwant %v", got, want)
	}
}

