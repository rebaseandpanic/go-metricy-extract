// TestIsGenerated_RejectsLookalikes pins the four strict regex invariants
// (start-anchor, `// ` prefix, exact casing, trailing `DO NOT EDIT.`). Any
// relaxation — case-insensitive flag, (?m) without ^, TrimSpace — would
// silently hide real files and this test would catch it.
func TestIsGenerated_RejectsLookalikes(t *testing.T) {
	cases := []struct {
		name, body string
	}{
		{"case mismatch", "// CODE GENERATED by x; DO NOT EDIT.\n"},
		{"triple slash", "/// Code generated by x; DO NOT EDIT.\n"},
		{"leading whitespace", "  // Code generated by x; DO NOT EDIT.\n"},
		{"missing do-not-edit", "// Code generated by x; autogen.\n"},
		{"no space after slashes", "//Code generated by x; DO NOT EDIT.\n"},
	}
	root := t.TempDir()
	for i, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			path := writeFile(t, root, filepath.Join("case", "c"+string(rune('a'+i))+".go"), tc.body)
			got, err := IsGenerated(path)
			if err != nil {
				t.Fatalf("IsGenerated: %v", err)
			}
			if got {
				t.Errorf("lookalike %q should NOT match the canonical marker", tc.body)
			}
		})
	}
}

