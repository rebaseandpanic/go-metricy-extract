// TestIsGenerated_GiantSingleLineDoesNotError pins the W1 fix: a 70 KiB
// single-line file triggers bufio.ErrTooLong inside Scanner, which we swallow
// and treat as "not generated". Regression guard against a revert that would
// abort the whole walk on one pathological file.
func TestIsGenerated_GiantSingleLineDoesNotError(t *testing.T) {
	root := t.TempDir()
	// 70 KiB of 'a' with no newline — exceeds maxGeneratedScanBytes (64 KiB),
	// forces Scanner to return ErrTooLong.
	body := strings.Repeat("a", 70*1024)
	path := writeFile(t, root, "giant.go", body)
	got, err := IsGenerated(path)
	if err != nil {
		t.Fatalf("IsGenerated should swallow bufio.ErrTooLong, got error: %v", err)
	}
	if got {
		t.Errorf("giant single-line file has no marker, should report false")
	}
}
